// Incriment and Decrement Operators Demo

public class Incriment {
    public static void main(String[] args) {
        int x = 0;

        /* Instructions:
         * 1. Take the Value Currently in 'x'
         * 2. Add 1 / Subtract 1 to / from it
         * 3. Assign the result back to 'x'
         */

        x++; System.out.println(x);
        x--; System.out.println(x);
    }
}
