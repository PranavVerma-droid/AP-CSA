// Demo Arithmetic Operations in Java

public class Arithmetic {
    public static void main(String[] args) {
        // int and int operations
        System.out.println(17 + 5);
        System.out.println(17 - 5);
        System.out.println(17 * 5);
        System.out.println(17 / 5);
        System.out.println(17 % 5);

        // double and double operations
        System.out.println(17.0 + 5.0);
        System.out.println(17.0 - 5.0);
        System.out.println(17.0 * 5.0);
        System.out.println(17.0 / 5.0);
        System.out.println(17.0 % 5.0);

        //int and double operations (mixed)
        System.out.println(17.0 + 5);
        System.out.println(17.0 - 5);
        System.out.println(17.0 * 5);
        System.out.println(17.0 / 5);
        System.out.println(17.0 % 5);
    }
}
